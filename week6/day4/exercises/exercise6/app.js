import chalk from 'chalk';

// Create colorful messages using chalk
console.log(chalk.blue('Hello, World!'));  // Blue text
console.log(chalk.green.bold('This is a bold green message.'));  // Bold green text
console.log(chalk.red.underline('This is an underlined red message.'));  // Underlined red text
console.log(chalk.bgYellow.black('This is a black text with yellow background!'));  // Black text with yellow background
